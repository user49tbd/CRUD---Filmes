package Entity;

public class Ator extends Pessoa{
	private String nomeArt;
	private int obra;
	private String nacio;
	public String getNomeArt() {
		return nomeArt;
	}
	public void setNomeArt(String nomeArt) {
		this.nomeArt = nomeArt;
	}
	public int getObra() {
		return obra;
	}
	public void setObra(int obra) {
		this.obra = obra;
	}
	public String getNacio() {
		return nacio;
	}
	public void setNacio(String nacio) {
		this.nacio = nacio;
	}
	
	
	
}
