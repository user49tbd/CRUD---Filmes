package Entity;

public class Temporada {
	private int obraCodigo;
	private String tipoObra;
	private int tempnum;
	private int totEp;
	private String snome;
	
	public String getSnome() {
		return snome;
	}
	public void setSnome(String snome) {
		this.snome = snome;
	}
	public int getObraCodigo() {
		return obraCodigo;
	}
	public void setObraCodigo(int obraCodigo) {
		this.obraCodigo = obraCodigo;
	}
	public String getTipoObra() {
		return tipoObra;
	}
	public void setTipoObra(String tipoObra) {
		this.tipoObra = tipoObra;
	}
	public int getTempnum() {
		return tempnum;
	}
	public void setTempnum(int tempnum) {
		this.tempnum = tempnum;
	}
	public int getTotEp() {
		return totEp;
	}
	public void setTotEp(int totEp) {
		this.totEp = totEp;
	}
	
}
